class World:
    def __init__(self):
        self.tile_list = []

        sky_img = pygame.image.load('Platformer sprites/Base pack/bg.png')
        grass_img = pygame.image.load('Platformer sprites/Base pack/Tiles/grassMid.png')
        dirt_img = pygame.image.load('Platformer sprites/Base pack/Tiles/grassCenter.png')

        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(dirt_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect, 1)
                    self.tile_list.append(tile)
                elif tile == 2:
                    img = pygame.transform.scale(grass_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect, 1)
                    self.tile_list.append(tile)

                elif tile == 3:
                    blocker = Enemy(col_count * tile_size, row_count * tile_size + 10)
                    blocker_group.add(blocker)

                    img = pygame.transform.scale(sky_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect, 0)
                    self.tile_list.append(tile)

                elif tile == 4:
                    spike = Spike(col_count * tile_size, row_count * tile_size)
                    spike_group.add(spike)

                    img = pygame.transform.scale(sky_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect, 0)
                    self.tile_list.append(tile)

                elif tile == 5:
                    platform_up = Platform_up(col_count * tile_size, row_count * tile_size)
                    platform_up_group.append(platform_up)

                    img = pygame.transform.scale(sky_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect, 0)
                    self.tile_list.append(tile)
                else:
                    img = pygame.transform.scale(sky_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect, 0)
                    self.tile_list.append(tile)
                col_count += 1
            row_count += 1

    def draw(self):
        for tile in self.tile_list:
            screen.blit(tile[0], tile[1])
